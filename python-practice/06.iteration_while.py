"""
# 파이썬의 while문, 쉽게 이해하기

while문은 "특정 조건이 참인 동안 계속해서 반복해라"라고 컴퓨터에게 지시하는 방법입니다. 어떤 조건이 만족될 때까지 같은 작업을 반복하고 싶을 때 사용합니다.

while문의 주요 특징:
- 조건이 참인 동안 계속해서 반복합니다
- 조건이 거짓이 되면 반복을 멈춥니다
- 조건이 처음부터 거짓이면 한 번도 실행되지 않습니다
- 조건이 절대 거짓이 되지 않으면 무한 반복(무한 루프)에 빠질 수 있으니 주의해야 합니다

일상생활에 비유하자면:
- "배가 부를 때까지 계속 먹는다"
- "해가 질 때까지 계속 놀이터에서 논다"
- "물이 끓을 때까지 기다린다"
- "숙제가 다 끝날 때까지 TV를 보지 않는다"

while문은 "특정 상황이 될 때까지 계속 이 작업을 반복해줘"라고 컴퓨터에게 말하는 것과 같습니다. 미리 몇 번 반복할지 정확히 알 수 없지만, 어떤 조건이 만족될 때까지 반복해야 할 때 유용하게 사용됩니다.
"""

# 새로운 쉬운 예시: 1부터 5까지 세기
count = 1
while count <= 5:
    print(f"현재 숫자: {count}")
    count += 1

print("카운트 완료!")


negative_comment_ratio = 0.03  # 초기 부정적 댓글 비율
threshold = 0.05              # 부정적 댓글 비율 임계값
comments_checked = 0

while negative_comment_ratio < threshold:
    # 소셜 미디어 댓글 확인 및 부정적 댓글 비율 업데이트
    comments_checked += 1
    print(f"댓글 {comments_checked}개 확인. 부정적 댓글 비율: {negative_comment_ratio:.2f}")
    # (가정) 댓글 확인 로직에 따라 negative_comment_ratio가 업데이트됨

print("부정적 댓글 비율이 임계값을 초과했습니다. 필요한 조치를 취합니다.")