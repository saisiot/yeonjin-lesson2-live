"""
# 파이썬의 리스트(List), 쉽게 이해하기

리스트는 여러 개의 항목을 순서대로 모아놓은 집합입니다.
실생활에서의 쇼핑 목록이나 할 일 목록과 비슷하다고 생각하면 됩니다.

리스트의 주요 특징:
- 여러 가지 항목을 순서대로 저장합니다.
- 각 항목은 번호(인덱스)로 위치를 찾을 수 있습니다.
- 첫 번째 항목은 0번부터 시작합니다 (컴퓨터는 항상 0부터 세기 시작합니다)
- 리스트는 언제든지 항목을 추가하거나 삭제할 수 있습니다
- 리스트에는 다양한 종류의 데이터(숫자, 글자, 다른 리스트 등)를 함께 저장할 수 있습니다

일상생활에 비유하자면:
- 책장에 책을 순서대로 나열해 놓은 것
- 학교 학급의 출석부처럼 학생들의 이름을 순서대로 정리한 것
- 장보기 목록에 구매할 물품들을 적어놓은 것

리스트는 여러 정보를 하나로 묶어서 관리하고 싶을 때 아주 유용합니다. "이 물건들을 순서대로 모아서 하나의 그룹으로 관리해줘"라고 컴퓨터에게 요청하는 방식입니다.
"""

meeting_schedule = ["팀 회의", "클라이언트 발표 준비", "캠페인 아이디어 회의"]
print(meeting_schedule)  # 출력: 팀 전략 회의

# 할일 리스트에 "보도자료 작성" 추가
meeting_schedule.append("보도자료 작성")

# 팀 전략회의에 들어가므로 첫 번째 할일 삭제
meeting_schedule.pop(0)

# 리스트 안 요소들의 순서 설명
print(meeting_schedule[0])  # 출력: 광고 캠페인 기획
print(meeting_schedule[1])  # 출력: 클라이언트 발표 준비
print(meeting_schedule[2])  # 출력: 홍보팀 협업
print(meeting_schedule[3])  # 출력: 보도자료 작성
