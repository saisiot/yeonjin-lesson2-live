""" 
# 파이썬의 딕셔너리(Dictionary), 쉽게 이해하기

딕셔너리는 '이름표와 물건'이 쌍으로 이루어진 저장 방식입니다.
실생활의 사전이나 주소록과 비슷하게 작동합니다.

딕셔너리는 아래와 같은 특징이 있습니다:
- '키(key)'와 '값(value)'의 쌍으로 데이터를 저장합니다
- 키를 통해 값을 빠르게 찾을 수 있습니다
- 각 키는 딕셔너리 안에서 유일해야 합니다 (중복 불가)
- 순서는 중요하지 않습니다

일상생활에 비유하자면:
- 학생증 번호(키)로 학생 정보(값)를 찾는 학교 데이터베이스
- 전화번호부에서 이름(키)으로 전화번호(값)를 찾는 것
- 사물함 번호(키)로 내용물(값)을 찾는 것

딕셔너리는 정보를 체계적으로 저장하고 찾고 싶을 때 아주 유용합니다. "이 이름표가 붙은 물건을 찾아줘"라고 컴퓨터에게 요청하는 방식이라고 생각하면 됩니다.
"""

