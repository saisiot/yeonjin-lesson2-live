"""
파이썬의 for문, 쉽게 이해하기
파이썬의 for문은 반복 작업을 할 때 사용하는 도구입니다. 컴퓨터에게 "이 목록에 있는 모든 것을 하나씩 차례대로 처리해줘"라고 지시하는 방법이라고 생각하면 됩니다.
for문은 마치 출석 부르기와 비슷합니다:

출석부(목록)에 있는 학생 이름을 하나씩 부르고
각 학생마다 같은 행동(출석 확인)을 합니다

기본 구조를 쉽게 풀어보면:

"for 물건 in 가방:" 이라고 하면, 가방에서 물건을 하나씩 꺼내는 것입니다
가방은 여러 물건이 들어있는 모음입니다 (목록, 문자열 등)
꺼낸 각 물건마다 정해진 작업을 반복합니다

일상생활에 비유하자면:

장보기 목록의 각 항목을 하나씩 확인하며 구매하는 것
편지의 각 글자를 하나씩 읽어나가는 것
책상 서랍의 물건들을 하나씩 꺼내서 정리하는 것

for문은 단순히 "이것들을 하나씩 꺼내서 같은 일을 반복해줘"라는 지시입니다. 컴퓨터가 지루한 반복 작업을 대신 해주는 편리한 도구라고 생각하시면 됩니다!
"""

campaigns = ["여름 세일", "신학기 프로모션", "연말연시 캠페인"]
for campaign in campaigns:
    print(f"캠페인 '{campaign}' 광고 문구 작성...")
    print(f"캠페인 '{campaign}' 예산 설정...")
    print(f"캠페인 '{campaign}' 타겟 고객층 분석...")

numbers = [1, 2, 3, 4, 5]
for number in numbers:
    print(f"{number}의 제곱은 {number ** 2}입니다.")
