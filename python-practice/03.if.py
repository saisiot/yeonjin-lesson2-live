"""
# 파이썬의 if문, 쉽게 이해하기

if문은 컴퓨터에게 "만약 이 조건이 맞다면, 이 작업을 해줘"라고 말하는 방법입니다.
일종의 결정을 내리는 도구라고 생각하면 됩니다.

if문은 우리가 일상에서 결정을 내리는 방식과 비슷합니다:
- 만약 비가 온다면, 우산을 가져갑니다
- 그렇지 않다면, 우산을 가져가지 않습니다

if문의 기본 구조는 이렇게 이해할 수 있습니다:
- "if 조건:" 부분은 "만약 이것이 사실이라면"이라는 의미입니다
- "else:" 부분은 "그렇지 않다면"이라는 의미입니다
- "elif 조건:" 부분은 "아니면, 만약 이것이 사실이라면"이라는 의미입니다

일상생활에 비유하자면:
- 만약 시간이 오후 6시 이후라면, 저녁을 먹습니다
- 만약 온도가 30도 이상이라면, 에어컨을 켭니다
- 만약 점수가 80점 이상이라면, "합격"이라고 알려줍니다

if문은 컴퓨터가 상황에 따라 다른 결정을 내리도록 하는 방법입니다. "이런 상황이면 이렇게, 저런 상황이면 저렇게 해줘"라고 컴퓨터에게 알려주는 것입니다.
"""

click_through_rate = 0.02  # 예시 값
target_rate = 0.025

if click_through_rate > target_rate:
    strategy = "A"
    
else:
    strategy = "B"
print(f"이번 주 전략: {strategy}")  # 출력: 이번 주 전략: B
